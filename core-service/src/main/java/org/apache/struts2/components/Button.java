package org.apache.struts2.components;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.struts2.views.annotations.StrutsTag;
import org.apache.struts2.views.annotations.StrutsTagAttribute;

import com.opensymphony.xwork2.util.ValueStack;

/**
 * <!-- START SNIPPET: javadoc -->
 * Creates an HTML &lt;div&gt;
 * <!-- END SNIPPET: javadoc -->
 *
 */
@StrutsTag(
    name="div",
    tldTagClass="org.apache.struts2.views.jsp.ui.S2ButtonTag",
    description="Render an HTML div",
    allowDynamicAttributes=true)
public class Button extends ClosingUIBean {

    public static final String TEMPLATE = "button";
    public static final String TEMPLATE_CLOSE = "button-close";
    public static final String COMPONENT_NAME = Button.class.getName();
    
    protected String type;

    public Button(ValueStack stack, HttpServletRequest request, HttpServletResponse response) {
        super(stack, request, response);
    }

    public String getDefaultOpenTemplate() {
        return TEMPLATE;
    }

    protected String getDefaultTemplate() {
        return TEMPLATE_CLOSE;
    }
    
    @StrutsTagAttribute(description="Specifies the html5 type element to display. e.g. button, reset", defaultValue="button")
    public void setType(String type) {
        this.type = type;
    }
}
